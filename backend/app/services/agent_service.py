    @classmethod
    def _asks_for_apm_list(cls, lower: str) -> bool:
        return any(phrase in lower for phrase in cls.APM_LIST_PHRASES) or ("apm" in lower and any(word in lower for word in ["lista", "listar", "muestra", "muéstrame", "tengo", "disponibles"]))

    @staticmethod
    def _asks_for_capabilities(lower: str) -> bool:
        return any(phrase in lower for phrase in ["herramientas", "tools", "capacidades", "qué puedes hacer", "que puedes hacer"])

    @classmethod
    def _needs_selected_entity(cls, lower: str) -> bool:
        return any(hint in lower for hint in cls.ENTITY_REQUIRED_HINTS)

    @classmethod
    def _is_vague_visualization_request(cls, lower: str, intents: list[Intent]) -> bool:
        if intents:
            return False
        if not any(word in lower for word in cls.VAGUE_VISUALIZATION_WORDS):
            return False
        if cls._references_previous_result(lower):
            return False
        return not any(word in lower for word in cls.GENERAL_WORDS)

    async def _apm_selection_response(self, request: ChatRequest, session_id: str, traces: list[ToolTraceItem], reason: str) -> ChatResponse:
        if not self.tools:
            result = self._credentials_required_response(session_id)
            result.tool_traces = traces
            return result
        start = time.perf_counter()
        response = await self.tools.list_apm_entities(account_ids=[request.account_id] if request.account_id else None)
        rows = response.data.get("rows", []) if response.data else []
        entities = [normalize_entity(row) for row in rows]
        traces.append(
            ToolTraceItem(
                tool="list_apm_entities",
                input={"account_id": request.account_id, "reason": reason},
                ok=response.ok,
                duration_ms=(time.perf_counter() - start) * 1000,
                safe_output_preview={"entities": len(entities), "all_returned": True},
            )
        )
        if not response.ok:
            answer = response.error.message if response.error else "No pude listar APMs desde New Relic."
            return ChatResponse(ok=False, session_id=session_id, answer=answer, tool_traces=traces, suggestions=self._suggestions())
        if not entities:
            answer = "No encontré APMs accesibles con las credenciales actuales. Verifica permisos, región y que las aplicaciones estén reportando en New Relic."
            return ChatResponse(ok=True, session_id=session_id, answer=answer, tool_traces=traces, entities=[], action="select_apm", suggestions=["Revisar sesión", "Buscar entidades"])

        if reason in {"entity_required", "data_sources_entity_required"}:
            answer = (
                f"Para responder esa consulta necesito una APM específica. Encontré {len(entities)} APMs accesibles; "
                "selecciona una en la lista completa y repetiré la consulta usando esa entidad como contexto."
            )
        else:
            answer = (
                f"Encontré {len(entities)} APMs accesibles. Te muestro todas en la lista interactiva. "
                "Selecciona una APM para fijarla como contexto del copiloto."
            )
        return ChatResponse(
            ok=True,
            session_id=session_id,
            answer=answer,
            tool_traces=traces,
            rows=[entity.model_dump(mode="json") for entity in entities],
            entities=entities,
            action="select_apm",
            suggestions=["Grafica throughput", "Grafica tiempo de respuesta", "Dame errores recientes", "Transacciones más lentas"],
        )

    def _capabilities_response(self, session_id: str, traces: list[ToolTraceItem]) -> ChatResponse:
        response = self.tools.tool_catalog() if self.tools else None
        rows = response.data.get("rows", []) if response else TOOL_CATALOG
        grouped: dict[str, list[str]] = {}
        for row in rows:
            grouped.setdefault(str(row.get("category", "general")), []).append(f"{row.get('name')}: {row.get('description')}")
        parts = ["Estas son las herramientas disponibles para operar sobre New Relic:"]
        for category, items in grouped.items():
            parts.append(f"\n{category.title()}: " + "; ".join(items[:10]))
        answer = "\n".join(parts)
        traces.append(ToolTraceItem(tool="tool_catalog", input={}, ok=True, duration_ms=0, safe_output_preview={"tools": len(rows)}))
        return ChatResponse(ok=True, session_id=session_id, answer=answer, tool_traces=traces, suggestions=self._suggestions())

    async def _data_sources_response(self, request: ChatRequest, session_id: str, traces: list[ToolTraceItem]) -> ChatResponse:
        if not self.tools:
            result = self._credentials_required_response(session_id)
            result.tool_traces = traces
            return result
        if not request.account_id:
            return self._account_required_response(session_id, traces)
        start = time.perf_counter()
        response = await self.tools.detect_apm_data_sources(request.account_id, request.entity_guid, since=request.time_range.since)
        traces.append(
            ToolTraceItem(
                tool="detect_apm_data_sources",
                input={"account_id": request.account_id, "entity_guid": request.entity_guid},
                ok=response.ok,
                duration_ms=(time.perf_counter() - start) * 1000,
                safe_output_preview={"sources": len(response.data.get("rows", [])) if response.data else 0},
            )
        )
        rows = response.data.get("rows", []) if response.data else []
        if rows:
            summary = ", ".join(f"{row.get('event_type')} ({row.get('count')} eventos)" for row in rows)
            answer = f"Detecté estas fuentes útiles para la APM seleccionada: {summary}. Usaré la fuente principal detectada para generar NRQL más preciso."
        else:
            answer = "No detecté eventos recientes para esta APM en el rango actual. Prueba ampliar el rango o revisar si la entidad está reportando."
        return ChatResponse(ok=response.ok, session_id=session_id, answer=answer, tool_traces=traces, rows=rows, suggestions=self._suggestions())

    async def _analyze_last_result_response(
        self,
        request: ChatRequest,
        session_id: str,
        traces: list[ToolTraceItem],
        clean_message: str,
        last_result: dict[str, Any] | None,
    ) -> ChatResponse:
        if not last_result:
            return ChatResponse(
                ok=True,
                session_id=session_id,
                answer=(
                    "No encuentro un gráfico o consulta anterior en esta conversación para analizar. "
                    "Genera primero una gráfica de New Relic, por ejemplo throughput, tiempo de respuesta o una vista general de métricas clave."
                ),
                tool_traces=traces,
                action="no_previous_result",
                suggestions=self._suggestions(),
            )
        rows = last_result.get("rows") or []
        visualizations = last_result.get("visualizations") or []
        nrql = str(last_result.get("nrql") or "")
        answer = await self._llm_explain(clean_message, rows, visualizations, nrql, traces, mode="analyze_last_result")
        if not answer:
            answer = self._metrics_answer(rows=rows, nrql=nrql, chart_count=len(visualizations), entity_name=request.entity_name, intents=[])
        return ChatResponse(
            ok=True,
            session_id=session_id,
            answer=answer,
            tool_traces=traces,
            visualizations=visualizations,
            rows=rows,
            nrql=nrql,
            action="analyze_last_result",
            suggestions=self._suggestions(),
        )

    async def _metrics_response(self, request: ChatRequest, session_id: str, traces: list[ToolTraceItem], clean_message: str, route: dict[str, Any]) -> ChatResponse:
        if not self.tools:
            result = self._credentials_required_response(session_id)
            result.tool_traces = traces
            return result
        if not request.account_id:
            return self._account_required_response(session_id, traces)

        lower = clean_message.lower()
        intents = self._choose_intents(clean_message, route)
        if self._is_vague_visualization_request(lower, intents):
            return self._clarification_response(session_id, route.get("clarifying_question") or self._default_clarification(request.entity_name), traces)
        if not intents:
            return self._clarification_response(session_id, route.get("clarifying_question") or self._default_clarification(request.entity_name), traces)
        if not request.entity_guid and self._needs_selected_entity_for_intents(intents):
            return await self._apm_selection_response(request, session_id, traces, reason="entity_required")

        all_rows: list[dict[str, Any]] = []
        all_visualizations: list[dict[str, Any]] = []
        nrql_parts: list[str] = []
        ok = True
        error_messages: list[str] = []

        for intent in intents[:5]:
            plan_nrql = NrqlBuilder.build(
                intent,
                app_name=request.entity_name,
                entity_guid=request.entity_guid,
                since=request.time_range.since,
                until=request.time_range.until,
                timezone=request.time_range.timezone or "UTC",
                event_type=request.transaction_event_type or "Transaction",
                transaction_name_attribute=request.transaction_name_attribute or "name",
                step=request.time_range.step,
            )
            start = time.perf_counter()
            tool_response = await self.tools.run_nrql(
                request.account_id,
                plan_nrql.nrql,
                entity_guid=request.entity_guid,
                title=plan_nrql.title,
                chart_type=plan_nrql.chart_type,
                time_range=request.time_range.model_dump(),
            )
            rows = tool_response.data.get("rows", []) if tool_response.data else []
            resolved_nrql = tool_response.nrql or plan_nrql.nrql
            fallback_used = bool(tool_response.data.get("original_nrql")) if tool_response.data else False
            traces.append(
                ToolTraceItem(
                    tool="run_nrql",
                    input={"account_id": request.account_id, "entity_guid": request.entity_guid, "intent": intent},
                    ok=tool_response.ok,
                    duration_ms=(time.perf_counter() - start) * 1000,
                    safe_output_preview={"rows": len(rows), "nrql": resolved_nrql, "fallback_used": fallback_used},
                )
            )
            nrql_parts.append(resolved_nrql)
            if tool_response.ok:
                all_rows.extend(rows)
                all_visualizations.extend(tool_response.visualizations)
                if self.db:
                    self.db.save_query_run(session_id, request.account_id, request.entity_guid, resolved_nrql, rows, tool_response.visualizations)
            else:
                ok = False
                if tool_response.error:
                    error_messages.append(tool_response.error.message)

        nrql_joined = "\n\n-- siguiente consulta --\n".join(nrql_parts)
        unique_errors = list(dict.fromkeys(error_messages))
        if ok or all_rows or all_visualizations:
            answer = await self._llm_explain(clean_message, all_rows, all_visualizations, nrql_joined, traces, mode="fresh_query")
            if not answer:
                answer = self._metrics_answer(rows=all_rows, nrql=nrql_joined, chart_count=len(all_visualizations), entity_name=request.entity_name, intents=intents)
            if not ok and unique_errors:
                answer += "\n\nNota: algunas consultas no respondieron para este rango y se omitieron del resultado final. " + " ".join(unique_errors[:2])
            final_ok = True
        else:
            final_ok = False
            answer = (
                "No pude generar una gráfica usable para ese rango. Probé consultas seguras contra New Relic, "
                "pero la API devolvió errores para todas. Prueba un rango un poco más corto o una métrica específica; "
                "también dejé las consultas en el panel técnico para revisarlas."
            )
            if unique_errors:
                answer += " Detalle: " + " ".join(unique_errors[:2])

        return ChatResponse(ok=final_ok, session_id=session_id, answer=answer, tool_traces=traces, visualizations=all_visualizations, rows=all_rows, nrql=nrql_joined, suggestions=self._suggestions())

    async def _llm_explain(
        self,
        question: str,
        rows: list[dict[str, Any]],
        visualizations: list[dict[str, Any]],
        nrql: str,
        traces: list[ToolTraceItem],
        mode: str,
    ) -> str | None:
        start = time.perf_counter()
        answer = await self.llm.explain_metrics(question=question, rows=rows, visualizations=visualizations, nrql=nrql, mode=mode)
        traces.append(
            ToolTraceItem(
                tool="llm_explain",
                input={"provider": self.llm.provider, "model": self.llm.model, "mode": mode, "row_count": len(rows), "chart_count": len(visualizations)},
                ok=bool(answer),
                duration_ms=(time.perf_counter() - start) * 1000,
                safe_output_preview={
                    "provider": self.llm.provider,
                    "model": self.llm.model,
                    "mode": mode,
                    "answer_chars": len(answer or ""),
                    "fallback_reason": None if answer else (self.llm.last_error or "LLM returned no explanation."),
                },
            )
        )
        return answer

    @staticmethod
    def _choose_intents(clean_message: str, route: dict[str, Any]) -> list[Intent]:
        raw_route_intents = route.get("intents") or []
        route_intents = [cast(Intent, item) for item in raw_route_intents if isinstance(item, str) and item in ALLOWED_INTENTS]
        if route_intents:
            return route_intents[:5]
        deterministic_intents = NrqlBuilder.infer_intents(clean_message)
        if deterministic_intents:
            return deterministic_intents[:5]
        lower = clean_message.lower()
        if any(word in lower for word in AgentService.GENERAL_WORDS):
            return ["golden_metrics"]
        return []

    @staticmethod
    def _needs_selected_entity_for_intents(intents: list[Intent]) -> bool:
        return bool(intents)

    @staticmethod
    def _default_clarification(entity_name: str | None = None) -> str:
        target = f" para {entity_name}" if entity_name else ""
        return (
            f"¿Qué métrica quieres graficar{target}? Puedo generar throughput, tiempo de respuesta, tasa de error, Apdex, percentiles, "
            "transacciones lentas, logs, trazas, base de datos, dependencias externas o una vista general de métricas clave."
        )

    def _clarification_response(self, session_id: str, question: str, traces: list[ToolTraceItem]) -> ChatResponse:
        return ChatResponse(
            ok=True,
            session_id=session_id,
            answer=question,
            tool_traces=traces,
            action="clarify_metric",
            suggestions=[
                "Grafica throughput minuto a minuto",
                "Grafica tiempo de respuesta minuto a minuto",
                "Grafica tasa de error minuto a minuto",
                "Dame una vista general de métricas clave",
                "Dame transacciones más lentas con p95 y p99",
            ],
        )

    @staticmethod
    def _metrics_answer(rows: list[dict[str, Any]], nrql: str, chart_count: int, entity_name: str | None = None, intents: list[Intent] | None = None) -> str:
        if not rows:
            return "New Relic no devolvió datos para esa consulta. Dejé la query visible para que puedas revisarla y probar un rango más amplio."
        target = f" para {entity_name}" if entity_name else ""
        requested = ", ".join((intents or []))
        suffix = f" Intención detectada: {requested}." if requested else ""
        return f"Ejecuté la consulta{target} contra New Relic y recibí {len(rows)} filas reales. Generé {chart_count} visualización(es) alineadas con tu pregunta.{suffix} Puedes ver/copiar el NRQL y exportar los datos."

    @staticmethod
    def _suggestions() -> list[str]:
        return [
            "Lista mis APMs",
            "Grafica throughput minuto a minuto",
            "Grafica tiempo de respuesta minuto a minuto",
            "Dame transacciones clave con p95, p99 y tasa de error",
            "Busca errores recientes por clase y mensaje",
            "Detecta fuentes de datos y atributos disponibles",
        ]

    @staticmethod
    async def sse_events(response: ChatResponse):
        yield f"event: message\ndata: {json.dumps({'type': 'answer_delta', 'content': response.answer})}\n\n"
        if response.nrql:
            yield f"event: tool\ndata: {json.dumps({'type': 'nrql', 'nrql': response.nrql})}\n\n"
        yield f"event: done\ndata: {response.model_dump_json()}\n\n"

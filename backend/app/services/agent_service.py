    def __init__(self, tools: NewRelicTools | None, db: Database | None = None) -> None:
        self.tools = tools
        self.db = db
        self.llm = SafeLLMService(get_settings())

    async def answer(self, request: ChatRequest) -> ChatResponse:
        clean_message = sanitize_user_text(request.message)
        session_id = self.db.ensure_chat_session(request.session_id, clean_message[:80], selected_entity_guid=request.entity_guid) if self.db else str(uuid.uuid4())
        if self.db:
            self.db.save_chat_message(session_id, "user", clean_message)

        traces: list[ToolTraceItem] = []
        history = self.db.get_chat_messages(session_id) if self.db else []
        last_result = self.db.get_latest_query_run(session_id, request.entity_guid) if self.db else None

        route = await self._llm_route(request, clean_message, traces, history, last_result)
        if route is None:
            route = self._fallback_route(clean_message, request, last_result)

        result = await self._execute_route(route, request, session_id, traces, clean_message, last_result)
        self._save_assistant(session_id, result)
        return result

    def _save_assistant(self, session_id: str, result: ChatResponse) -> None:
        if self.db:
            self.db.save_chat_message(session_id, "assistant", result.answer, result.model_dump(mode="json"))

    async def _llm_route(
        self,
        request: ChatRequest,
        clean_message: str,
        traces: list[ToolTraceItem],
        history: list[dict[str, Any]],
        last_result: dict[str, Any] | None,
    ) -> dict[str, Any] | None:
        start = time.perf_counter()
        route = await self.llm.route_request(
            message=clean_message,
            context={
                "account_id": request.account_id,
                "entity_guid": request.entity_guid,
                "entity_name": request.entity_name,
                "time_range": request.time_range.model_dump(),
            },
            history=history,
            last_result=last_result,
        )
        duration_ms = (time.perf_counter() - start) * 1000
        preview: dict[str, Any] = {
            "provider": self.llm.provider,
            "model": self.llm.model,
            "llm_enabled": self.llm.enabled,
            "has_previous_query_result": bool(last_result),
        }
        if route:
            preview.update(
                {
                    "action": route.get("action"),
                    "intents": route.get("intents", []),
                    "confidence": route.get("confidence"),
                    "needs_tool": route.get("needs_tool"),
                    "answer_from_llm": bool(route.get("answer")),
                }
            )
        else:
            preview["fallback_reason"] = self.llm.last_error or "LLM returned no valid route."
        traces.append(
            ToolTraceItem(
                tool="llm_router",
                input={
                    "provider": self.llm.provider,
                    "model": self.llm.model,
                    "message_chars": len(clean_message),
                    "has_account": bool(request.account_id),
                    "has_selected_apm": bool(request.entity_guid),
                },
                ok=route is not None,
                duration_ms=duration_ms,
                safe_output_preview=preview,
            )
        )
        return route

    def _fallback_route(self, clean_message: str, request: ChatRequest, last_result: dict[str, Any] | None) -> dict[str, Any]:
        """Secondary path used only when the LLM is disabled or fails."""
        lower = clean_message.lower()
        if self._references_previous_result(lower) and last_result:
            return {"action": "analyze_last_result", "intents": [], "answer": None, "confidence": 0.4, "needs_tool": True}
        if self._asks_for_capabilities(lower):
            return {"action": "capabilities", "intents": [], "answer": None, "confidence": 0.4, "needs_tool": True}
        if self._is_greeting(lower):
            target = f" sobre {request.entity_name}" if request.entity_name else ""
            return {
                "action": "answer_direct",
                "answer": (
                    f"¡Hola! Soy OpsPilot, tu copiloto para New Relic{target}. "
                    "Puedo analizar APMs, ejecutar NRQL seguro, generar gráficas y explicar resultados dentro del contexto de observabilidad."
                ),
                "intents": [],
                "confidence": 0.3,
                "needs_tool": False,
            }
        if self._is_thanks(lower):
            return {
                "action": "answer_direct",
                "answer": "Con gusto. Puedo seguir ayudándote con APMs, errores, latencia, throughput, deploys, logs, trazas o NRQL en New Relic.",
                "intents": [],
                "confidence": 0.3,
                "needs_tool": False,
            }
        if self._asks_identity(lower):
            return {
                "action": "answer_direct",
                "answer": (
                    "Soy OpsPilot for New Relic: un copiloto de observabilidad. "
                    "Primero interpreto tu intención con el LLM y, si hacen falta datos reales, ejecuto herramientas seguras contra New Relic."
                ),
                "intents": [],
                "confidence": 0.3,
                "needs_tool": False,
            }
        if self._is_conceptual_new_relic_question(lower):
            return {"action": "answer_direct", "answer": self._fallback_contextual_answer(clean_message), "intents": [], "confidence": 0.3, "needs_tool": False}
        if self._is_off_topic(lower):
            return {"action": "new_relic_only", "answer": None, "intents": [], "confidence": 0.3, "needs_tool": False}
        if self._asks_for_apm_list(lower):
            return {"action": "list_apms", "intents": [], "answer": None, "confidence": 0.4, "needs_tool": True}
        if any(phrase in lower for phrase in ["fuentes de datos", "data sources", "inventario de datos", "qué eventos", "que eventos", "atributos disponibles"]):
            return {"action": "data_sources", "intents": [], "answer": None, "confidence": 0.4, "needs_tool": True}

        deterministic_intents = NrqlBuilder.infer_intents(clean_message)
        if deterministic_intents:
            return {"action": "run_nrql", "intents": deterministic_intents, "answer": None, "confidence": 0.4, "needs_tool": True}
        if any(word in lower for word in self.GENERAL_WORDS):
            return {"action": "run_nrql", "intents": ["golden_metrics"], "answer": None, "confidence": 0.4, "needs_tool": True}
        if self._is_vague_visualization_request(lower, []):
            return {"action": "clarify", "intents": [], "clarifying_question": self._default_clarification(request.entity_name), "confidence": 0.4, "needs_tool": False}
        return {"action": "clarify", "intents": [], "clarifying_question": self._default_clarification(request.entity_name), "confidence": 0.2, "needs_tool": False}

    async def _execute_route(
        self,
        route: dict[str, Any],
        request: ChatRequest,
        session_id: str,
        traces: list[ToolTraceItem],
        clean_message: str,
        last_result: dict[str, Any] | None,
    ) -> ChatResponse:
        action = route.get("action")
        if action == "answer_direct":
            answer = route.get("answer") or await self.llm.answer_contextual(question=clean_message, context={"entity_name": request.entity_name, "account_id": request.account_id})
            return ChatResponse(ok=True, session_id=session_id, answer=answer or self._fallback_contextual_answer(clean_message), tool_traces=traces, suggestions=self._suggestions())

        if action == "new_relic_only":
            answer = route.get("answer") or (
                "Puedo ayudarte, pero mantengo este chat enfocado en New Relic y observabilidad. "
                "Reformula tu pregunta hacia APMs, métricas, errores, logs, trazas, deploys, alertas o NRQL."
            )
            return ChatResponse(ok=True, session_id=session_id, answer=answer, tool_traces=traces, action="new_relic_only", suggestions=self._suggestions())

        if action == "capabilities":
            return self._capabilities_response(session_id, traces)

        if action == "list_apms":
            return await self._apm_selection_response(request, session_id, traces, reason="llm_route_list")

        if action == "data_sources":
            if not request.entity_guid:
                return await self._apm_selection_response(request, session_id, traces, reason="data_sources_entity_required")
            return await self._data_sources_response(request, session_id, traces)

        if action == "analyze_last_result":
            return await self._analyze_last_result_response(request, session_id, traces, clean_message, last_result)

        if action == "clarify":
            question = route.get("clarifying_question") or self._default_clarification(request.entity_name)
            return self._clarification_response(session_id, question, traces)

        if action == "run_nrql":
            return await self._metrics_response(request, session_id, traces, clean_message, route)

        return self._clarification_response(session_id, self._default_clarification(request.entity_name), traces)

    @classmethod
    def _is_greeting(cls, lower: str) -> bool:
        stripped = lower.strip(" !¡¿?.")
        if stripped in cls.GREETING_WORDS:
            return True
        if cls._has_new_relic_term(lower):
            return False
        return len(stripped.split()) <= 3 and any(stripped.startswith(word + " ") for word in cls.GREETING_WORDS)

    @classmethod
    def _is_thanks(cls, lower: str) -> bool:
        stripped = lower.strip(" !¡¿?.")
        return stripped in cls.THANKS_WORDS

    @classmethod
    def _asks_identity(cls, lower: str) -> bool:
        return any(phrase in lower for phrase in cls.IDENTITY_PHRASES)

    @classmethod
    def _has_new_relic_term(cls, lower: str) -> bool:
        return any(term in lower for term in cls.NEW_RELIC_TERMS)

    @classmethod
    def _is_conceptual_new_relic_question(cls, lower: str) -> bool:
        if not cls._has_new_relic_term(lower):
            return False
        return lower.strip().startswith(cls.CONCEPTUAL_PREFIXES)

    @classmethod
    def _is_off_topic(cls, lower: str) -> bool:
        if cls._has_new_relic_term(lower):
            return False
        return any(hint in lower for hint in cls.OFF_TOPIC_HINTS)

    @classmethod
    def _references_previous_result(cls, lower: str) -> bool:
        return any(phrase in lower for phrase in cls.PREVIOUS_RESULT_WORDS)

    @staticmethod
    def _fallback_contextual_answer(question: str) -> str:
        lower = question.lower()
        if "apdex" in lower:
            return "Apdex en New Relic mide satisfacción de usuarios con base en tiempos de respuesta: satisfecho, tolerable y frustrado. Es útil para priorizar degradaciones de experiencia junto con latencia, throughput y tasa de error."
        if "throughput" in lower or "rpm" in lower:
            return "Throughput en New Relic representa volumen de solicitudes o transacciones, normalmente en requests por minuto. Sirve para correlacionar carga con latencia, errores y saturación."
        if "nrql" in lower:
            return "NRQL es el lenguaje de consulta de New Relic para leer datos de telemetría. En OpsPilot lo uso de forma read-only y con builders seguros para evitar consultas destructivas o fuera de contexto."
        if "error" in lower:
            return "En New Relic conviene analizar errores por tasa, clase, mensaje, transacción afectada y cambio temporal. Así puedes distinguir picos aislados de regresiones o fallos persistentes."
        return "Puedo explicarlo dentro del contexto de New Relic y observabilidad. Para convertirlo en datos reales, selecciona una cuenta/APM y pide una métrica, una gráfica o una consulta NRQL segura."

